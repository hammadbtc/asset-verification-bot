// Check if inscription is an Ordinal Egg
export function isOrdinalEgg(inscriptionId) {
    // Handle both full ID and just the number part
    const shortId = inscriptionId.split('i')[0];
    return ORDINAL_EGGS_IDS.has(inscriptionId) || 
           Array.from(ORDINAL_EGGS_IDS).some(id => id.startsWith(shortId + 'i'));
}

// Get all IDs for debugging
export function getAllOrdinalEggIds() {
    return Array.from(ORDINAL_EGGS_IDS);
}
